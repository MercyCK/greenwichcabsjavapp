    public void Show_Report_In_Table() {
        DecimalFormat money= new DecimalFormat("Â£###,##0.00");
        
        ArrayList<Report> list = getReportList();
        DefaultTableModel model = (DefaultTableModel) jTable_Journeys.getModel();
        
        model.setRowCount(0);
        Object[] row = new Object[4];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getdriverid();
            row[1] = money.format(list.get(i).getTaking());
            totalDay+= list.get(i).getTaking();
            row[2] = money.format(list.get(i).getPercentageDriver());
            expectedDay+=list.get(i).getPercentageDriver();
            row[3] = list.get(i).getJobsDone();
            totalJobsDay+=list.get(i).getJobsDone();

            model.addRow(row);

        }
     
     
        //jLabelDate.setText(addDate);
        txtStartTime.setText(String.valueOf(money.format(totalDay)));
        txtJobsDone.setText(String.valueOf(totalJobsDay));
        txtExpectedDay.setText(String.valueOf(money.format(expectedDay)));
    }
    
