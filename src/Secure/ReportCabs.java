    public ArrayList<Report> getReportList() {

        ArrayList<Report> reportList = new ArrayList<Report>();
        
        conn = MySqlConnect.ConnectDB();
        String query = "SELECT DRIVERID, SUM(FARE) AS TAKINGD, SUM(FARE)*0.2 AS PERCENDRIV, COUNT(DRIVERID) AS JOBSD FROM JOURNEY WHERE DATEOFJOURNEY = ? GROUP BY DRIVERID";

        PreparedStatement st;
        ResultSet rs;

        try {
            //st = conn.createStatement();
            st = conn.prepareStatement(query);
            st.setString(1, addDate);
            rs = st.executeQuery();
            Report report;

            while (rs.next()) {
                //Float.parseFloat(rs.getString("fare")) 
                //DB FIELDS
                report = new Report(rs.getInt("DRIVERID"), rs.getFloat("TAKINGD"), rs.getFloat("PERCENDRIV"), rs.getInt("JOBSD"));
                reportList.add(report);
            }

        } catch (SQLException ex) {
            Logger.getLogger(UpdateJourney.class.getName()).log(Level.SEVERE, null, ex);
        }
        return reportList;
    }

